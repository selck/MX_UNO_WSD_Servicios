package mx.com.amx.mx.uno.feed.ws.dto;

public class NoticiaExtraRSSDTO {
	private String url_nota;
    private String nombre;
    private String titulo;
    private String fuente;
    private String descripcion;
    private String descripcion_categoria;
    private String descripcion_seccion;
    private String descripcion_tipo_seccion;
    private String imagen_principal;
    private String pie_imagen;
    
	public String getUrl_nota() {
		return url_nota;
	}
	public void setUrl_nota(String url_nota) {
		this.url_nota = url_nota;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getFuente() {
		return fuente;
	}
	public void setFuente(String fuente) {
		this.fuente = fuente;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getDescripcion_categoria() {
		return descripcion_categoria;
	}
	public void setDescripcion_categoria(String descripcion_categoria) {
		this.descripcion_categoria = descripcion_categoria;
	}
	public String getDescripcion_seccion() {
		return descripcion_seccion;
	}
	public void setDescripcion_seccion(String descripcion_seccion) {
		this.descripcion_seccion = descripcion_seccion;
	}
	public String getDescripcion_tipo_seccion() {
		return descripcion_tipo_seccion;
	}
	public void setDescripcion_tipo_seccion(String descripcion_tipo_seccion) {
		this.descripcion_tipo_seccion = descripcion_tipo_seccion;
	}
	public String getImagen_principal() {
		return imagen_principal;
	}
	public void setImagen_principal(String imagen_principal) {
		this.imagen_principal = imagen_principal;
	}
	public String getPie_imagen() {
		return pie_imagen;
	}
	public void setPie_imagen(String pie_imagen) {
		this.pie_imagen = pie_imagen;
	}
    
    
    
    
    
}
